/**
 * Translate a key with optional variable interpolation.
 * @param {string} lang - "ms" or "en"
 * @param {string} key  - dot-separated path e.g. "dashboard.welcome"
 * @param {object} vars - replacement values e.g. { name: "Ali" }
 */
export function t(lang, key, vars = {}) {
    const parts = key.split(".");
    let val = strings[lang] ?? strings.ms;
    for (const part of parts) {
        val = val?.[part];
        if (val === undefined) break;
    }
    if (typeof val !== "string") return key;
    return val.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

export default strings;
